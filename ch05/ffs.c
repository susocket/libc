/**
 * int ffs(int i);
 * ffs()会由低位至高位，判断参数i字节中的每一位，将最先出现位1的位置返回。
 *  返回值：1到32之间，表示位的值最先出现真值的位置。返回值若为0，即表示
 *  没有一个位的值为真，该字节为0.
 *
 **/

#include <stdio.h>
#include <string.h>

void main(){
    int i[] = {0, 1, 2, 4, 8, 16, 32, 64};
    int n;
    for(n = 0; n < 8; n++){
        printf("%d: %d\n", i[n], ffs(i[n]));
    }
}

/**
0: 0
1: 1
2: 2
4: 3
8: 4
16: 5
32: 6
64: 7
**/